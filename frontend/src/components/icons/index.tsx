// Helper to merge base size with additional classes
function cn(base: string, additional?: string): string {
  if (!additional) return base;
  // If additional includes size classes (w- or h-), use additional only
  if (/\bw-\d|h-\d/.test(additional)) return additional;
  return `${base} ${additional}`;
}

